package interfaces;

import java.util.EventListener;

/**
 * interface pour les écouteurs des fleches
 * @author léo-paul Lapointe
 *
 */
public interface FlecheListener extends EventListener{
	
	/**
	 * écoute si oui ou non la fleche du haut est appuyé
	 * @param allumer
	 */
	public void flecheHaut(boolean allumer);
	
	/**
	 * écoute si oui ou non la fleche du bas est appuyé
	 * @param allumer
	 */
	public void flecheBas(boolean allumer);

	/**
	 * écoute si oui ou non la fleche de gauche est appuyé
	 * @param allumer
	 */
	public void flecheGauche(boolean allumer);

	/**
	 * écoute si oui ou non la fleche de droite est appuyé
	 * @param allumer
	 */
	public void flecheDroite(boolean allumer);

}
